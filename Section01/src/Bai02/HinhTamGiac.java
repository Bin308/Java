package Bai02;

public class HinhTamGiac {
	public static float chuVi(float canhGocVuongT1, float canhGocVuongT2, float canhHuyen) {
		return canhGocVuongT1 + canhGocVuongT2 + canhHuyen;
	}

	public static float dienTich(float canhGocVuongT1, float canhGocVuongT2) {
		return (canhGocVuongT1 + canhGocVuongT2) / 2;

	}
}
