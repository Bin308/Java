package Bai3;

public class tinhToan {
	public static int tong(int a1, int b1) {
		return a1 + b1;
	}

	public static int hieu(int a2, int b2) {
		return a2 - b2;
	}

	public static int tich(int a3, int b3) {
		return a3 * b3;
	}

	public static int thuong(int a4, int b4) {
		return a4 / b4;
	}

	public static double binhPhuong(int a) {
		return Math.pow(a, 2);
	}

	public static double canBac2(double a) {
		return Math.sqrt(a);
	}

	public static double tichPiVaA(double a) {
		return (Math.PI * a);
	}
}
